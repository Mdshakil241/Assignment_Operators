document.write(x);

// Assignment Operators (-)
var x = 3;
x -= 6; // x = x - 6
document.write(x);

// Assignment Operators (*)
var x = 3;
x *= 6; // x = x * 6
document.write(x);

// Assignment Operators (*)
var x = 3;
x /= 6; // x = x / 6
document.write(x);


